import turtle
#Параметры черепахи
turtle.title("Turtle Drawing")
t = turtle.Turtle()
t.shape("turtle")
t.pensize(10)
t.pencolor("black")
t.speed(100)
#Рисуем глаза
t.penup()
t.goto(-300, 0)
t.pendown()
t.circle(50)
t.penup()
t.goto(-300, -100)
t.pendown()
t.circle(50)
#Рисуем нос
t.penup()
t.goto(-220, 0)
t.pendown()
t.left(-30)
t.forward(100)
t.left(180)
t.forward(100)
t.left(-120)
t.forward(100)
#Рисуем усы
t.penup()
t.goto(-50, -100)
t.left(150)
t.pendown()
for i in range(13):
    t.forward(3)
    t.right(6)
t.forward(60)
t.left(45)
t.forward(30)
t.right(115)
t.forward(30)
t.left(45)
t.forward(60)
for i in range(13):
    t.forward(3)
    t.right(6)
#Рисуем рот
t.penup()
t.goto(-10, -100)
t.pendown()
t.left(91)
t.forward(190)
t.right(90)
t.pencolor("red")
t.forward(30)
for i in range(100):
    t.forward(2)
    t.right(1.8)
t.forward(30)

turtle.exitonclick()